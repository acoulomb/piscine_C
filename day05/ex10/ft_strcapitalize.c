char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			if ((str[i - 1] >= 0 && str[i - 1] <= 47) ||
			(str[i] >= 58 && str[i] <= 64) || str[i - 1] >= 123)
				str[i] -= 32;
		if (str[i + 1] >= 'A' && str[i + 1] <= 'Z')
			str[i + 1] += 32;
		i++;
	}
	return (str);
}
