void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long int n;

	n = (long int)nb;
	if (n < 0)
	{
		ft_putchar('-');
		n = -nb;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + '0');
}
