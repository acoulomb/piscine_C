char	*ft_rot42(char *str)
{
	int	rot;
	int x;

	rot = 42;
	x = 0;
	while (str[x])
	{
		if ('a' <= str[x] && str[x] <= 'z')
			str[x] = ((str[x] - 'a' + rot) % 26) + 'a';
		if ('A' <= str[x] && str[x] <= 'Z')
			str[x] = ((str[x] - 'A' + rot) % 26) + 'A';
		x++;
		if (!(('a' <= str[x]) && (str[x] <= 'z')) ||
		(('A' <= str[x]) && (str[x] <= 'Z')))
			x++;
	}
	return (str);
}
