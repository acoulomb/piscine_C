int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;

	i = 0;
	while (tab[i])
	{
		while (f(i, i + 1) <= 0)
		{
			i++;
			if (i == length)
				return (1);
		}
	}
	return (0);
}
