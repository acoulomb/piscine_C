int		ft_atoi(char *str)
{
	int	x;
	int neg;
	int numb;

	x = 0;
	numb = 0;
	neg = 1;
	while ((str[x] == ' ') || (str[x] == '\t') || (str[x] == '\n'))
		x++;
	while ((str[x] == '\v') || (str[x] == '\f') || (str[x] == '\r'))
		x++;
	if (str[x] == '+')
		x++;
	if (str[x] == '-')
	{
		neg = -1;
		x++;
	}
	while (str[x] >= '0' && str[x] <= '9')
	{
		numb = numb * 10 + (str[x] - '0');
		x++;
	}
	return (numb * neg);
}
